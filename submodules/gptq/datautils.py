def get_loaders(
    name, nsamples=128, seed=0, seqlen=2048, model='',load_train=False
):
    if 'wikitext2' in name:
        return get_wikitext2(nsamples, seed, seqlen, model,load_train)
    if 'ptb' in name:
        if 'new' in name:
            return get_ptb_new(nsamples, seed, seqlen, model,load_train)
        return get_ptb(nsamples, seed, seqlen, model,load_train)
    if 'c4' in name:
        if 'new' in name:
            return get_c4_new(nsamples, seed, seqlen, model,load_train)
        return get_c4(nsamples, seed, seqlen, model,load_train)
